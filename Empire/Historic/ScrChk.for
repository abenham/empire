C 
	FUNCTION SCRCHK(Z6)
C
C DETERMINES IF Z6 IS IN CURRENT SCREEN SECTOR SHOWING
C	0=NO, 1=YES
C
	INCLUDE "EMPIRE.INC/NOLIST"
C
	IF (MODE.EQ.1) GOTO 100
	SCRCHK=1
	GOTO 400
100	JECT=JECTOR
	SCRCHK=0
	IY=(Z6-1)/100
	ICityLocation=Z6-IY*100
	IF (JECT.GT.4) GOTO 200  'CHECK CityLocation COORD FIRST
	IF (ICityLocation.GT.SWIDTH) GOTO 400
	GOTO 300
200	IF (ICityLocation.LT.(100-SWIDTH)) GOTO 400
	JECT=JECT-5
300	IF ((IY.LT.(JECT*10)).OR.(IY.GT.(JECT*10+19))) GOTO 400
	SCRCHK=1			'PASSED, IT"S GOOD
400	RETURN
	END