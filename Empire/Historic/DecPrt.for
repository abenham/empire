C
	SUBROUTINE DECPRT(N)
C
C PRINT INTEGER N, WITH MINIMAL LEADING BLANKS
C
	IMPLICIT INTEGER(A-Z)
C
	IF (N.LT.0) TYPE 999
999	FORMAT("+-",$)
	J=IABS(N)
	IF (J.GE.10000) GOTO 500
	IF (J.GE.1000) GOTO 400
	IF (J.GE.100) GOTO 300
	IF (J.GE.10) GOTO 200
100	TYPE 998,J
998	FORMAT("+",I1,$)
	RETURN
C
200	TYPE 997,J
997	FORMAT("+",I2,$)
	RETURN
C
300	TYPE 996,J
996	FORMAT("+",I3,$)
	RETURN
C
400	TYPE 995,J
995	FORMAT("+",I4,$)
	RETURN
C
500	TYPE 994,J
994	FORMAT("+",I5,$)
	RETURN
	END