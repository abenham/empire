	SUBROUTINE FTRCTY(Z6,MRANGE,LOC,IFO,ILA,ID)
	INCLUDE "EMPIRE.INC/NOLIST"

	ID=1000
	IGARBG=RND(70+UpperIndex(15))+1		'**
	DO 2500 ILOOP=IGARBG,IGARBG+70+UpperIndex(15)
	IA=ILOOP
	IF (IA.GT.70+UpperIndex(15)) IA=IA-70-UpperIndex(15)
	IF (IA.GT.70) GOTO 2400
	IF (CityOwner(IA).NE.2) GOTO 2500
	IF (IDIST(Z6,CityLocation(IA)).GT.MRANGE) GOTO 2500
	IF (IDIST(CityLocation(IA),LOC).GE.ID) GOTO 2500
	IFO=7
	ILA=CityLocation(IA)
	ID=IDIST(CityLocation(IA),LOC)
	GOTO 2500
2400	IB=IA-70
	IF (UnitLocation(2800+IB).EQ.0) GOTO 2500
	IF (IDIST(Z6,UnitLocation(2800+IB)).GT.MRANGE) GOTO 2500
	IF (IDIST(UnitLocation(2800+IB),LOC).GE.ID) GOTO 2500
	IF ((IDIST(Z6,UnitLocation(2800+IB)).GT.MRANGE-MRANGE/2)
	1   .AND.(UnitAction(1300+IB).NE.9)) GOTO 2500
	IFO=6
	ILA=IB
	ID=IDIST(UnitLocation(2800+IB),LOC)
2500	CONTINUE
	RETURN
	END