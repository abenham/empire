C 
	FUNCTION JIGGLE(Z6,NUM)
C
C DO RANDOM MOVE FOR PLAYER"S ARMY
C
	INCLUDE "EMPIRE.INC/NOLIST"
C
	DO 100 I=1,9
100	AB9(I)=ReferenceMap(Z6+MoveOffset(I+1))		'**
	IF (AB9(9).NE."T") GOTO 200
	JIGGLE=0
	MYCODE(NUM)=0
	RETURN
C 
200	DO 300 I1=1,9
300	IF ((AB9(I1).EQ."*").OR.(AB9(I1).EQ."X")) GOTO 400
	I1=9
400	DO 500 I2=1,9
500	IF ((AB9(I2).GE."a").AND.(AB9(I2).LE."t")) GOTO 600
	I2=9
600	DO 700 I3=1,9
700	IF (AB9(I3).EQ."T") GOTO 800
	I3=9
800	M1=RND(8)+1				'**
	M2=M1+7
	DO 900 I4=M1,M2
	I5=ICORR(I4)
	I=Z6+MoveOffset(I5+1)			'**
900	IF ((ORDER(I).EQ.0).AND.(AB9(I5).EQ."+")) GOTO 1000
	I4=0
1000	M=I1
	IF (M.EQ.9) M=I3
	IF (M.EQ.9) M=I2
	IF (M.EQ.9) M=I5
	IF (I4.EQ.0) M=9
	JIGGLE=M
	RETURN
	END